#5. Realiza a leitura de 1 float referente ao valor de um produto e imprime o valor com descontos de 10%, 20% e 50%.

valorProduto = float(input("Digite o valor do produto: R$"))

print(f"O valor com desconto de 10% é R${valorProduto*0.90} \nO valor com desconto de 20% é R${valorProduto*0.80} \nO valor com desconto de 50% é R${valorProduto*0.50}")