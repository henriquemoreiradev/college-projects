#7. Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

alturaQuadrado = float(input("Digite a altura do quadrado: "))
baseQuadrado = float(input("Digite a base do quadrado: "))

areaQuadrado = alturaQuadrado*baseQuadrado

print(f"O dobro da área do quadrado é {areaQuadrado*2}")